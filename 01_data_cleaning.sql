-------------- 5. NEGATIVE SALES / QUANTITY --------------
SELECT COUNT(*) AS NEGATIVE_RECORDS
FROM PROJECT.SUPERSTORE
WHERE SALES < 0 
   OR QUANTITY <= 0;


-------------- 6. SALES SUMMARY  --------------
SELECT 
    ROUND(MIN(SALES), 2)  AS MIN_REVENUE,
    ROUND(MAX(SALES), 2)  AS MAX_REVENUE,
    ROUND(AVG(SALES), 2)  AS AVG_REVENUE,
    ROUND(SUM(SALES), 2)  AS TOTAL_REVENUE
FROM PROJECT.SUPERSTORE;


-------------- 7. PROFIT SUMMARY  --------------
SELECT 
    ROUND(MIN(PROFIT), 2) AS MIN_PROFIT,
    ROUND(MAX(PROFIT), 2) AS MAX_PROFIT,
    ROUND(AVG(PROFIT), 2) AS AVG_PROFIT,
    ROUND(SUM(PROFIT), 2) AS TOTAL_PROFIT,
    round(SUM(PROFIT)/SUM(SALES) * 100, 2) AS PROFIT_MARGIN
FROM PROJECT.SUPERSTORE;