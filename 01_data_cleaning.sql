-------------- 2. CHECK NULLS IN KEY BUSINESS COLUMNS ---------------
SELECT 
	SUM(SALES IS NULL)        AS SALES_NULLS,
	SUM(PROFIT IS NULL)       AS PROFIT_NULLS,
	SUM(ORDER_DATE IS NULL)   AS ORDER_DATE_NULLS,
	SUM(CUSTOMER_ID IS NULL)  AS CUSTOMER_ID_NULLS
FROM PROJECT.SUPERSTORE;

-- NULL PERCENTAGE FOR ORDER_ID
SELECT 
	COUNT(*) AS TOTAL_ROWS,
	SUM(ORDER_ID IS NULL) AS NULL_COUNT,
	ROUND((SUM(ORDER_ID IS NULL) / COUNT(*)) * 100, 2) AS NULL_PERCENTAGE
FROM PROJECT.SUPERSTORE;


-------------- 3. IDENTIFY DUPLICATES --------------
WITH DUPLICATES AS (
    SELECT *,
           ROW_NUMBER() OVER (PARTITION BY ORDER_ID, CUSTOMER_ID, PRODUCT_ID, ORDER_DATE, SALES, PROFIT, QUANTITY ORDER BY ROW_ID) AS RN
    FROM PROJECT.SUPERSTORE
)

DELETE FROM PROJECT.SUPERSTORE
WHERE ROW_ID IN (
    SELECT ROW_ID 
    FROM DUPLICATES 
    WHERE RN > 1
);
